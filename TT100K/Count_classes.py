import os

def collect_class_ids(label_dir):
    class_ids = set()
    for filename in os.listdir(label_dir):
        if filename.endswith(".txt"):
            with open(os.path.join(label_dir, filename), "r") as f:
                for line in f:
                    if line.strip():
                        cls_id = line.strip().split()[0]
                        class_ids.add(cls_id)
    return class_ids

